#O comando print
#Separação entre os argumentos é espaço por default
print('Higor', 'Stefany')
#Usa-se a palavra reservada sep='', para substituir a serparação por outra coisa
print('Higor', 'Stefany', sep='-')
#Ao final do print, por default, é colocado um enter (\n) no final da linha, que faz com que dê um enter entre um print e outro. Isso pode ser mmudado com a palavra
#reservada end='', que substitui esse parametro por outro a sua escolha
print('Higor', 'Stefany', sep='-', end='')

